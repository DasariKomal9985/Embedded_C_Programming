/**
 ******************************************************************************
 * @brief ADC Conversion Complete Callback
 *
 * 🧠 This function automatically executes when:
 * ADC conversion completes.
 *
 * Flow:
 * ADC Conversion Complete
 *        ↓
 * EOC Flag Set
 *        ↓
 * ADC IRQ Generated
 *        ↓
 * NVIC Executes ISR
 *        ↓
 * HAL_ADC_ConvCpltCallback()
 *
 ******************************************************************************
 */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc) {
	/* ✅ Check interrupt source */
	if (hadc->Instance == ADC1) {
		/* 📥 Read ADC converted value */
		adc_val = HAL_ADC_GetValue(hadc);

		/* 🔢 Convert ADC value to voltage
		 *
		 * Formula:
		 * Voltage = (ADC_Value × 3.3V) / 4095
		 */
		voltage = (adc_val * 3.3f) / 4095.0f;

		/* 🌡️ LM35 Formula
		 *
		 * LM35 gives:
		 * 10mV per °C
		 *
		 * Example:
		 * 0.25V = 25°C
		 */
		temperature = voltage * 100.0f;

		/* 🚩 Set flag for main loop */
		adc_flag = 1;

		/* 🔁 Restart ADC interrupt conversion */
		HAL_ADC_Start_IT(&hadc1);
	}
}

